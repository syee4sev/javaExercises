public class Mathematics {



    public int addMethod(int num1, int num2){
//        return addition.addMethod(num1, num2);
        return num1 + num2;
    }

    public int subMethod(int num1, int num2){
//        return subtraction.subMethod(num1, num2);
        return num1 - num2;
    }

    public int multMethod(int num1, int num2){
//        return multiplication.multMethod(num1, num2);
        return num1 * num2;
    }

    public int divMethod(int num1, int num2){
//        return division.divMethod(num1, num2);
        return num1 / num2;
    }








}
